    public void cargarDatos() {
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.setColumnIdentifiers(labels);

        List<Cliente> listaClientes = clienteService.getClientes();

        String row[] = new String[7];
        for (Cliente cliente : listaClientes) {

            row[0] = "" + cliente.getId();
            row[1] = cliente.getNombre();
            row[2] = cliente.getApellido1();
            row[3] = cliente.getApellido2();
            row[4] = cliente.getCorreo();
            row[5] = cliente.getTelefono();
            row[6] = cliente.getDireccion();

            tabla.addRow(row);
        }
        tblClientes.setModel(tabla);

    }

    public String validarCampos() {

        if (txtCedula.getText().trim().equals("")) {
            txtCedula.requestFocus();
            return "Debe ingresar un número de cédula";
        } else if (txtCedula.getText().length() != 9) {
            txtCedula.requestFocus();
            return "La cédula debe ser de nueve digitos";
        } else {
            try {
                Integer.parseInt(txtCedula.getText());
            } catch (NumberFormatException ex) {
                txtCedula.requestFocus();
                return "La cédula debe ser solo numeros";
            }
        }
        if (txtNombre.getText().trim().equals("")) {
            txtNombre.requestFocus();
            return "Debe ingresar el nombre";
        }
        if (txtApellido1.getText().trim().equals("")) {
            txtApellido1.requestFocus();
            return "Debe ingresar el primer apellido";
        }
        if (txtApellido2.getText().trim().equals("")) {
            txtApellido2.requestFocus();
            return "Debe ingresar el segundo apellido";
        }
        if (txtCorreo.getText().trim().equals("")) {
            txtCorreo.requestFocus();
            return "Debe ingresar el correo";
        }
        if (txtTelefono.getText().trim().equals("")) {
            txtTelefono.requestFocus();
            return "Debe ingresar el teléfono";
        }
        if (txtDireccion.getText().trim().equals("")) {
            txtDireccion.requestFocus();
            return "Debe ingresar la direccion";
        }

        return "";
    }
