    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String error = validarCampos();

        if (error.equals("")) {

            Cliente cliente = new Cliente();
            cliente.setId(Integer.parseInt(txtCedula.getText()));
            cliente.setNombre(txtNombre.getText());
            cliente.setApellido1(txtApellido1.getText());
            cliente.setApellido2(txtApellido2.getText());
            cliente.setCorreo(txtCorreo.getText());
            cliente.setTelefono(txtTelefono.getText());
            cliente.setDireccion(txtDireccion.getText());

            if (winCliente.getTitle().equals("Guardar")) {

                if (clienteService.save(cliente)) {
                    JOptionPane.showMessageDialog(winCliente, "Cliente guardado exitosamente");
                }
            } else {
                if (clienteService.update(cliente)) {
                    JOptionPane.showMessageDialog(winCliente, "Cliente editado exitosamente");
                }
            }
            winCliente.dispose();
            cargarDatos();

        } else {
            JOptionPane.showMessageDialog(winCliente, error, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tblClientes.getSelectedRow();
        if (fila != -1) {
            winCliente.setTitle("Editar");
            winCliente.setSize(350, 450);
            winCliente.setLocationRelativeTo(this);
            img = new ImageIcon(getClass().getResource("/icons/edit.png"));
            winCliente.setIconImage(img.getImage());
            winCliente.setVisible(true);
            txtCedula.setEditable(false);

            txtCedula.setText(tblClientes.getValueAt(fila, 0).toString());
            txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
            txtApellido1.setText(tblClientes.getValueAt(fila, 2).toString());
            txtApellido2.setText(tblClientes.getValueAt(fila, 3).toString());
            txtCorreo.setText(tblClientes.getValueAt(fila, 4).toString());
            txtTelefono.setText(tblClientes.getValueAt(fila, 5).toString());
            txtDireccion.setText(tblClientes.getValueAt(fila, 6).toString());
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblClientes.getSelectedRow();
        if (fila != -1) {

            int id = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());

            Cliente cliente = new Cliente();
            cliente.setId(id);
            cliente.setNombre(tblClientes.getValueAt(fila, 1).toString());
            cliente.setApellido1(tblClientes.getValueAt(fila, 2).toString());
            cliente.setApellido2(tblClientes.getValueAt(fila, 3).toString());
            cliente.setCorreo(tblClientes.getValueAt(fila, 4).toString());
            cliente.setTelefono(tblClientes.getValueAt(fila, 5).toString());
            cliente.setDireccion(tblClientes.getValueAt(fila, 6).toString());

            int opcion = JOptionPane.showConfirmDialog(null, "Seguro que desea eliminar esta columna?",
                    "Eliminar",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (opcion == 0) {
                if (clienteService.delete(cliente)) {
                    JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente");
                }
                cargarDatos();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

