    public String validarCampos() {

        if (cmbUsuario.getSelectedIndex() == 0) {
            cmbUsuario.requestFocus();
            return "Debe seleccionar un usuario";
        }
        if (txtPassword.getPassword().length == 0) {
            txtPassword.requestFocus();
            return "Debe ingresar una contraseña";
        }
        return "";
    }

    public void llenarUsuarios() {

        cmbUsuario.setSelectedIndex(0);
        cmbUsuario.removeAllItems();
        cmbUsuario.addItem("Seleccione");
        for (Usuario usuario : usuarioService.getUsuarios()) {

            cmbUsuario.addItem(usuario.getDescripcion());
        }
    }
