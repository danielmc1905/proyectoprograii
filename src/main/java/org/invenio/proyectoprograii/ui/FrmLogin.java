    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String error = validarCampos();
        if (error.equals("")) {
            String descripcion = cmbUsuario.getSelectedItem().toString();
            String password = new String(txtPassword.getPassword());
            Usuario usuario = usuarioService.validarUsuario(descripcion, password);
            if (usuario != null) {
                this.dispose();
                FrmEscritorio frmEscritorio = new FrmEscritorio(ctx);
                frmEscritorio.setUsuario(usuario);
                frmEscritorio.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Contraseña incorrecta");
            }
        } else {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

